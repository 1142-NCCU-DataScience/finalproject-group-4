# ==============================================================================
# 10. 模型分類能力驗證：聚類穩定性評估 (Cluster Stability via Bootstrap)
# ==============================================================================
# 邏輯參考 R 語言的 clusterboot (fpc package)
# 透過重複抽樣驗證：原本被分在同一個特型（槍兵/盲砲/強棒）的打者，在新的抽樣中是否依然穩定綁在一起。
print("\n--- 開始進行分群穩定度驗證 (Bootstrap Stability Analysis) ---")

n_iterations = 100  # 拔靴法重複抽樣次數
stability_scores = []

# 拿你在步驟 5 已經訓練好的 KMeans 模型標籤作為基準 (Base Labels)
base_labels = df["cluster"].values

for i in range(1, n_iterations + 1):
    # 1. 有放回的隨機重抽樣 (Bootstrap)
    X_resampled, resampled_indices = resample(
        X_scaled,
        np.arange(len(X_scaled)),
        random_state=i + 42  # 確保每次隨機不重複且可復現
    )

    # 2. 用一模一樣的模型參數去訓練抽樣後的資料
    kmeans_boot = KMeans(
        n_clusters=best_k,
        random_state=i + 42,
        n_init=40,
        max_iter=100
    )
    kmeans_boot.fit(X_resampled)

    # 3. 讓新模型回頭預測「原始完整資料」的標籤
    pred_labels_on_base = kmeans_boot.predict(X_scaled)

    # 4. 計算原始分群與新分群之間的相似度 (ARI 分數)
    # ARI 分數範圍為 -1 到 1。越接近 1 代表重抽樣後分群結構越穩定、越不容易被打亂。
    score = adjusted_rand_score(base_labels, pred_labels_on_base)
    stability_scores.append(score)

    if i % 20 == 0:
        print(f"已完成 {i}/{n_iterations} 次模擬抽樣...")

# ==============================================================================
# 11. 輸出驗證結果與繪圖
# ==============================================================================
mean_stability = np.mean(stability_scores)
print(f"\n【驗證結論】")
print(f"經過 {n_iterations} 次 Bootstrap 模擬，平均分群穩定度 (ARI): {mean_stability:.3f}")

# 解讀指南
if mean_stability >= 0.75:
    print("👉 結論：分群結構【極度穩定】！這四種打者型態（工兵、槍兵、盲砲、強棒）是資料中非常扎實的特徵結構。")
