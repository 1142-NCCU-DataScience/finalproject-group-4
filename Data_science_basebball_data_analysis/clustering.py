"""找打者的function

"""

def find_replacement_batters(
    df,
    target_name,
    target_year,
    top_n=10,
    same_cluster=True
):
    similarity_features = [
        "AVG", "OBP", "SLG", "OPS",
        "ISO", "BB%", "K%",
        "wOBA", "wRC+"
    ]

    missing_cols = [col for col in similarity_features if col not in df.columns]
    if missing_cols:
        raise ValueError(f"缺少欄位: {missing_cols}")

    if "cluster" not in df.columns:
        raise ValueError("資料中沒有 cluster 欄位，請先完成 KMeans clustering。")

    work_df = df.copy()

    X = work_df[similarity_features].copy()
    X = X.replace([np.inf, -np.inf], np.nan)
    X = X.fillna(X.median())

    scaler = StandardScaler()
    X_scaled = scaler.fit_transform(X)

    scaled_cols = [col + "_scaled" for col in similarity_features]

    for i, col in enumerate(scaled_cols):
        work_df[col] = X_scaled[:, i]

    target_rows = work_df[
        (work_df["Name"].str.contains(target_name, case=False, na=False)) &
        (work_df["Year"] == target_year)
    ]

    if len(target_rows) == 0:
        raise ValueError("找不到這位打者在該年份的資料，請確認姓名或年份。")

    target = target_rows.iloc[0]
    target_cluster = target["cluster"]

    if same_cluster:
        candidate_pool = work_df[
            (work_df["Year"] == target_year) &
            (work_df["cluster"] == target_cluster) &
            (work_df["Name"] != target["Name"])
        ].copy()
    else:
        candidate_pool = work_df[
            (work_df["Year"] == target_year) &
            (work_df["Name"] != target["Name"])
        ].copy()

    if len(candidate_pool) == 0:
        raise ValueError("沒有找到可比較的候選打者。")

    target_vector = target[scaled_cols].values.reshape(1, -1)
    candidate_vectors = candidate_pool[scaled_cols].values

    candidate_pool["distance"] = pairwise_distances(
        candidate_vectors,
        target_vector,
        metric="euclidean"
    ).flatten()

    result_cols = [
        "Name", "Year", "cluster", "type", "distance",
        "AVG", "OBP", "SLG", "OPS", "ISO",
        "BB%", "K%", "wOBA", "wRC+",
        "HR", "WAR"
    ]

    available_cols = [col for col in result_cols if col in candidate_pool.columns]

    return candidate_pool.sort_values("distance")[available_cols].head(top_n)

"""**找出前十位相近打者**"""

